SELECT RTRIM(COPTD.UDF05) 客户编号, RTRIM(TD001)+'-'+RTRIM(TD002)+'-'+RTRIM(TD003) 订单号, RTRIM(TD004) 品号, RTRIM(TD005) 品名, RTRIM(TD006) 规格, 
CAST(TD008 AS FLOAT) 订单数量, RTRIM(TD053) 配置方案, RTRIM(TQ003) 配置描述, RTRIM((CASE COPTD.UDF08 WHEN '' THEN TD005 ELSE COPTD.UDF08 END)) 保友品名, RTRIM(COPTD.UDF10) 产品代码, 
RTRIM(REPLACE(REPLACE(REPLACE(REPLACE(TD020, '菜鸟条码:', ''), '菜鸟条码：', ''), '京东条码:', ''), '京东条码：', '')) 描述备注, ISNULL(MBU01, '') 纸箱尺寸, 
SUBSTRING(TD013, 1, 4)+'-'+SUBSTRING(TD013, 5, 2)+'-'+SUBSTRING(TD013, 7, 2) 检验日期 
FROM COPTC 
INNER JOIN COPTD ON TC001 = TD001 AND TC002 = TD002 
LEFT JOIN COPTQ ON TQ001 = TD004 AND TQ002 = TD053 
LEFT JOIN ( 
	SELECT TA026, TA027, TA028, MAX(MBU01) MBU01 FROM ( 
		SELECT TA026, TA027, TA028, INVMB.MBU01 AS MBU01 
		FROM MOCTA INNER JOIN MOCTB ON TA001 = TB001 AND TA002 = TB002 
		INNER JOIN INVMB ON MB001 = TB003 
		WHERE 1=1 AND INVMB.MBU01 IS NOT NULL 
	) AS A 
	GROUP BY TA026, TA027, TA028 
) AS MOCTA ON TA026 = TD001 AND TA027 = TD002 AND TA028 = TD003 
WHERE 1=1 
AND RTRIM(TD001)+'-'+RTRIM(TD002)+'-'+RTRIM(TD003) IN ('2212-9001021-0013', '2210-065570-0002', '2212-9000041-0002' ) 





