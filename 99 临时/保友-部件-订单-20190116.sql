SELECT DISTINCT 
RTRIM(TD001) + '-' + RTRIM(TD002) + '-' + RTRIM(TD003) 订单号, 
MB001 品号, MB002 品名, MB003 规格, MB025 品号属性, 
TD008 数量, TD011 单价, CONVERT(FLOAT, TD008)*CONVERT(FLOAT, TD011) 金额, 
TC003 订单日期, TC039 单据日期, TC027 审核码, 
(CASE TD016 WHEN 'N' THEN '未结束' WHEN 'Y' THEN '自动结束' WHEN 'y' THEN '自动结束' ELSE TD016 END) 结束
FROM COPTC
INNER JOIN COPTD ON TC001 = TD001 AND TC002 = TD002
INNER JOIN INVMB ON TD004 = MB001
WHERE 1 = 1
AND ((TC003 BETWEEN '20180901' AND '20190131') OR (TC039 BETWEEN '20180901' AND '20190131'))
AND TC027 = 'Y'
AND TC004 = '0118'
AND TC001 = '2202'
ORDER BY TC003, RTRIM(TD001) + '-' + RTRIM(TD002) + '-' + RTRIM(TD003)