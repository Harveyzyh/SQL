SELECT TD001 订单别, RTRIM(TD002) 订单号, RTRIM(TD003) 订单序号, RTRIM(TH001) 销货单别, RTRIM(TH002) 销货单号, RTRIM(TH003) 销货单序号, 
TC004 客户编号, 
TD013 预交货日期, (CASE WHEN TG003 IS NULL THEN '' ELSE TG003 END) 销货日期, 
(CASE WHEN (TG003 > TD013 OR TG003 IS NULL) THEN '1' ELSE '' END) 超期 
FROM COPTC
LEFT JOIN COPTD ON TD001 = TC001 AND TD002 = TC002
LEFT JOIN COPTH ON TH014 = TD001 AND TH015 = TD002 AND TH016 = TD003
LEFT JOIN COPTG ON TG001 = TH001 AND TG002 = TH002 

WHERE 1=1
AND TD013 BETWEEN '20180101' AND '20181231'
AND TC004 IN ('0081','0094','2323','0098','0092','0106','0085','0087','0050')
ORDER BY TD001, TD002, TD003