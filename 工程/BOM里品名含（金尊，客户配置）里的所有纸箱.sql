--事项：刘红梅：BOM里品名含（金尊，客户配置）里的所有纸箱

SELECT A.MB001 主件品号, A.MB002 主件品名, A.MB003 主件规格, B.CB005 纸箱品号, B.MB003 纸箱规格, MW002 纸箱工艺 FROM 
(SELECT MB001, MB002, MB003 FROM INVMB
WHERE MB002 LIKE '金尊%客户配置'
AND MB109 <> 'N') AS A ,

(SELECT CB005, MB003, CB011, CB001 FROM BOMCB
LEFT JOIN INVMB ON MB001 = CB005 
WHERE CB005 LIKE '4010%'
--AND CB001 = '25004568'
) AS B

LEFT JOIN CMSMW ON MW001 = B.CB011
WHERE A.MB001 = B.CB001
ORDER BY A.MB001, B.CB005