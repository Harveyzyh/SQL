SELECT TD001 订单别, TD002 订单号, TD003 订单序号, TD004 订单品号, TD053 客户配置, CAST(TD008 AS FLOAT) 订单数量, 
CAST(TD009 AS FLOAT) 已交数量, TD016 订单结束码, LRPFLAG 已跑计划, LRPDATE 计划时间, LRPCOUNT 计划次数, 
TA001 工单别, TA002 工单号, CAST(TA015 AS FLOAT) 工单预计产量, TA006 工单品号
FROM COPTD 
LEFT JOIN MOCTA ON TA026 = TD001 AND TA027 = TD002 AND TA028 = TD003 AND TA006 = TD004
WHERE TD002 = '016961'
ORDER BY TD001, TD002, TD003



