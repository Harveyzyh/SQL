INNER JOIN MOCTA ON TB001 = TA001 AND TB002 = TA002 
LEFT JOIN COPTH ON TA026 = TH014 AND TA027 = TH015 AND TA028 = TA016 AND TA006 = TH004
WHERE NOT EXISTS ( SELECT 1 FROM MOCTE WHERE TB001 = TE011 AND TE012 = TB002 AND TB003 = TE004 AND TB006 = TE009 AND TE019 = 'Y' ) 
-- AND TH020 = 'N'
-- AND TB018 <> 'V'
-- AND (TB004 - TB005 ) > 0
AND TB002 = '19070249'
AND TB004 != 0
ORDER BY W01, W02
