--品号库存单头与单身不等
USE COMFORT
SELECT RTRIM(MB001) 品号, MB019 核准状况, MB025 材料属性, MB064 单头库存, C.TOTAL 单身库存 FROM INVMB
LEFT JOIN
(SELECT INVMC.MC001, SUM(INVMC.MC007) AS TOTAL FROM INVMC 
--LEFT JOIN CMSMC ON INVMC.MC002 = CMSMC.MC001
--WHERE CMSMC.MC005 = 'Y'
GROUP BY INVMC.MC001) AS C  ON MC001 = MB001
WHERE MB064 > C.TOTAL
AND MB109 <> 'N'
AND MB025 = 'P'
--AND C.TOTAL = 0
ORDER BY MB001


