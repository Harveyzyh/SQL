SELECT LLXA001, LLXA002, LLXA003, LLXA009, LLXA010, LLXA011, LLXA012, LLXA013, LLXA015
, TC001, TC002, TE003, TE004, TE009, TE010
FROM LL_LYXA 
LEFT JOIN 
(
	SELECT TC001, TC002, TE003, TE004, TE011, TE012, TE009, TE010 FROM MOCTC INNER JOIN MOCTE ON TC001 =TE001 AND TC002 = TE002
)
AS MOCTC ON TC001 = LLXA001 AND TC002 = LLXA002 AND LLXA009 = TE011 AND LLXA010 = TE012 AND LLXA011 = TE009 AND LLXA012 = TE004
WHERE 1=1
AND TC001 IS NULL
AND SUBSTRING(LL_LYXA.CREATE_DATE, 1, 8) >= '20200101'
ORDER BY LL_LYXA.CREATOR, LLXA001, LLXA002, LLXA003