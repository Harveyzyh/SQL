SELECT DISTINCT 
RTRIM(TA026) AS 订单别, RTRIM(TA027) AS 订单号, RTRIM(TA028) AS 订单序号, RTRIM(TA006) AS 产品品号, RTRIM(TQ002) AS 客户配置, RTRIM(TQ003) AS 配置描述, 
RTRIM(TA001) AS 工单别, RTRIM(TA002) AS 工单号, TA015 预计产量, 
RTRIM(TB003) AS 品号, RTRIM(MB002) AS 品名, TB006 AS 工艺, RTRIM(MW002) 工艺名称, 
TB009 AS 工单仓位,CONVERT(FLOAT, TB004) AS 工单需领用量, 
(CONVERT(FLOAT, TB004) - CONVERT(FLOAT, TB005)) AS 工单未领用量
FROM MOCTA
LEFT JOIN COPTQ ON TQ001 = TA006 AND TQ002 = TA073
INNER JOIN MOCTB ON TA001 = TB001 AND TA002 = TB002
INNER JOIN INVMB ON TB003 = MB001
INNER JOIN CMSMW ON MW001 = TB006

WHERE 1=1
AND TA001 = '5101'
AND TA002 = '21032860' -- 工单号
ORDER BY RTRIM(TA001), RTRIM(TA002), TB006, RTRIM(TB003)