SELECT RTRIM(MB1.MB001) 成品品号, RTRIM(MB1.MB002) 成品品名, RTRIM(MB1.MB003) 成品规格, RTRIM(MB1.UDF12) 系列, 
RTRIM(MB2.MB001) 材料品号, RTRIM(MB2.MB002) 材料品名, RTRIM(MB2.MB003) 材料规格, 
RTRIM(MW001) 工艺, RTRIM(MW002) 工艺名称, CAST(MW003 AS VARCHAR(100)) 工作组, CB008 单用量
FROM BOMCB_List
INNER JOIN INVMB AS MB1 ON MB1.MB001 = CB001 
INNER JOIN INVMB AS MB2 ON MB2.MB001 = CB005
INNER JOIN CMSMW ON MW001 = CB011

ORDER BY CB001, CB005, CB011