SELECT TL001 核价单别, TL002 核价单号, TL003 审核日期, TL004 供应商编号, TL006 单头审核码, 
TM003 核价序号, TM004 核价品号, TM010 核价单价, TM011 单身审核码, TM014 核价生效日, TM015 核价失效日, 
MB011 品号供应商单价, MB014 品号供应商生效日, MB015 品号供应商失效日  
FROM PURTL 
INNER JOIN PURTM ON TL001 = TM001 AND TL002 = TM002 
LEFT JOIN PURMB ON MB002 = TL004 AND MB001 = TM004 AND MB008 = TL010  -- 品号供应商单头档

WHERE 1=1
AND TL010 = '20200526' 
-- AND MB011 IS NULL
ORDER BY TL001, TL002, TM003 