SELECT (CASE TA079 WHEN '1' THEN '蓝字' WHEN '2' THEN '红字' ELSE TA079 END) 单据类型, 
(CASE TA087 WHEN '1' THEN '未核销' WHEN '2' THEN '部分核销' WHEN '3' THEN '已核销' ELSE TA087 END) 核销状态, 
(CASE TA031 WHEN 'Y' THEN '已生成' WHEN 'N' THEN '未生成' WHEN 'V' THEN '无需生成' ELSE TA031 END) 生成分录, 
SUBSTRING(TA034, 1, 4) + '-' + SUBSTRING(TA034, 5, 2) + '-' + SUBSTRING(TA034, 7, 2) 单据日期, 
SUBSTRING(TA003, 1, 4) + '-' + SUBSTRING(TA003, 5, 2) + '-' + SUBSTRING(TA003, 7, 2) 审核日期,
TA008 币种, TA009 汇率, TA014 发票号码, TA015 发票日期,  
TA080 红冲,  (CASE WHEN (TA081 IS NOT NULL AND TA081 != '' ) THEN RTRIM(TA081) + '-' + RTRIM(TA082) ELSE RTRIM(TA081) END) 红冲发票号, 
(CASE TA010 WHEN 'S' THEN '可抵扣专用发票' WHEN 'B' THEN '普通发票' WHEN 'T' THEN '运输发票' WHEN 'N' THEN '不可抵扣专用发票' WHEN 'A' THEN '农产品收购凭证' WHEN 'G' THEN '海关代征完税凭证' WHEN 'W' THEN '废旧物资收购凭证' WHEN 'Z' THEN '其他' ELSE TA010 END) 发票类型,
(CASE TA011 WHEN '1' THEN '应税内含' WHEN '2' THEN '应税外加' WHEN '3' THEN '零税率' WHEN '4' THEN '免税' WHEN '9' THEN '不计税' ELSE TA011 END) 税种, 
RTRIM(TB001) 采购发票单别, RTRIM(TB002) 采购发票单号, RTRIM(TB003) 序号, 
(CASE TB004 WHEN '1' THEN '进货' WHEN '2' THEN '退货' WHEN '3' THEN '委外进货' WHEN '4' THEN '委外退货' 
WHEN '5' THEN '报关/赎单费用' WHEN '6' THEN '出口费用' WHEN '7' THEN '资产取得' WHEN '8' THEN '在建工程' 
WHEN '9' THEN '其他' WHEN 'A' THEN '冲减预开发票' WHEN 'B' THEN '采购' WHEN 'C' THEN '维修' 
WHEN 'E' THEN '资产进货' WHEN 'G' THEN 'CRM维修' ELSE TB004 END)来源, 
TB005 来源单别, TB006 来源单号, TB007 来源序号, 
(CASE WHEN (TB008 IS NOT NULL AND TB008 != '' ) THEN SUBSTRING(TB008, 1, 4) + '-' + SUBSTRING(TB008, 5, 2) + '-' + SUBSTRING(TB008, 7, 2) ELSE RTRIM(TB008) END) 来源单据审核日期, 
TA004 供应商编号, PURMA.MA002 供应商简称, PURMA.MA003 供应商全称, 
TB009 应付金额, TB011 备注, TB012 审核码, TB013 科目编号, ACTMA.MA003 科目简称, TB015 原币税前金额, 
TB016 原币税额, TB017 本币税前金额, TB018 本币税额, TB019 开票数量, TB020 开票单价, TB037 品号, TB038 品名, TB039 规格, TB040 单位,TB041 进货单价, TB046 送货单号, TB049 本次开票原币税前金额, TB050 本次开票本币税前金额, TB051 费用结算码, TB052 税率, 
TB053 归属存货成本, TB054 原币差额, TB055 本币差额

FROM ACPTA
INNER JOIN ACPTB ON TB001 = TA001 AND TB002 = TA002 
LEFT JOIN PURMA ON PURMA.MA001 = TA004 --供应商编号
LEFT JOIN ACTMA ON ACTMA.MA001 = TB013 --科目编号
WHERE 1=1
-- AND TA001 = '7101'
-- AND TA003 BETWEEN '20100101' AND  '20151231'
AND TA004 = 'A0111'
ORDER BY RTRIM(TB001), RTRIM(TB002), RTRIM(TB003)


