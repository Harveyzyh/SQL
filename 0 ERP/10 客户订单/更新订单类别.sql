UPDATE COPTC SET COPTC.UDF12 = 
				(CASE 
				WHEN COPTC.TC001 IN ('2212') THEN '电商' 
				WHEN COPTC.TC001 IN('2201', '2204', '2205', '2210', '2213', '2214') THEN '内销' 
				WHEN COPTC.TC004='0118' AND COPTC.TC001 IN ('2202', '2203', '2211') THEN '内销'
				ELSE '外销' 
				END ) 
				
				
				
SELECT COPTC.UDF12, SC002 
-- UPDATE WG_DB..SC_PLAN SET SC002 = COPTC.UDF12
FROM COPTC 
INNER JOIN COPTD ON TC001 = TD001 AND TC002 = TD002
INNER JOIN WG_DB..SC_PLAN ON SC001 = RTRIM(TD001)+'-'+RTRIM(TD002)+'-'+RTRIM(TD003)
WHERE COPTC.UDF12 != SC002