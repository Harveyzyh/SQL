FROM COPTR 
INNER JOIN BOMMD ON TR004 = MD001 AND TR009 = MD003 AND TR030 = MD002
WHERE 1=1
AND (TR010 != MD006 OR TR011 != MD007 OR MD009 != TR013 OR MD011 != TR015 OR MD012 != TR016 )

-- AND TR001 = '10890101'
