SELECT SUBSTRING(TF003, 1, 4) + '-' + SUBSTRING(TF003, 5, 2) 入库年月, TG004 品号, TG005 品名, TG006 规格,  SUM(TG011) 入库数量
FROM MOCTF
INNER JOIN MOCTG ON TF001 = TG001 AND TF002 = TG002 
WHERE 1=1
AND TF006 = 'Y' 
AND TF003 >= '20170101'
AND TG004 LIKE '1%'
GROUP BY SUBSTRING(TF003, 1, 4) + '-' + SUBSTRING(TF003, 5, 2), TG004, TG005, TG006
ORDER BY SUBSTRING(TF003, 1, 4) + '-' + SUBSTRING(TF003, 5, 2), TG004