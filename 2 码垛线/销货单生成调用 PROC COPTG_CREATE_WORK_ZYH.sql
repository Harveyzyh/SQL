ALTER PROCEDURE dbo.COPTG_CREATE_WORK_ZYH AS 
BEGIN
	DECLARE @PrintIdC INT, @TG001C VARCHAR(4), @TG004C VARCHAR(4)
	IF EXISTS (SELECT * FROM ROBOT_TEST.dbo.PrintData WHERE STATUSS = 0 AND OutFlag = 0) 
	BEGIN
		SELECT @PrintIdC = PrintId, @TG001C = TG001, @TG004C = TG004 FROM 
		(
			SELECT TOP 1 PrintId, TG001, TG004 FROM ROBOT_TEST.dbo.PrintData WHERE STATUSS = 0 AND OutFlag = 0 ORDER BY Create_Date 
		) AS A0
		EXEC [192.168.0.99].COMFORT.dbo.COPTG_CREATE_ZYH @PrintId = @PrintIdC, @TG001 = @TG001C, @TG004 = @TG004C, @TH007 = 'P031'
	END
END