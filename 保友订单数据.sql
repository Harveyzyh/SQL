SELECT DISTINCT TOP 2000
RTRIM(COPTD.TD001) AS COPTD001, RTRIM(COPTD.TD002) AS COPTD002, RTRIM(COPTD.TD003) AS COPTD003, RTRIM(COPTD.TD004) AS COPTD004, 
RTRIM(PURTD.TD001) AS PURTD001, RTRIM(PURTD.TD002) AS PURTD002, RTRIM(PURTD.TD003) AS PURTD003, PURTC.TC004
FROM COPTC 
INNER JOIN COPTD ON COPTD.TD001 = COPTC.TC001 AND COPTD.TD002 = COPTC.TC002
INNER JOIN PURTD ON PURTD.TD013 = COPTD.TD001 AND PURTD.TD021 = COPTD.TD002 AND PURTD.TD023 = COPTD.TD003 
INNER JOIN PURTC ON PURTC.TC001 = PURTD.TD001 AND PURTC.TC002 = PURTD.TD002 
WHERE 1=1 
AND PURTC.TC014 = 'Y' -- 采购审核码
-- AND PURTC.TC004 = 'A0048'




