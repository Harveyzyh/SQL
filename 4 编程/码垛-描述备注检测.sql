SELECT SUBSTRING(KK.CREATE_DATE, 1, 4) + '-' + SUBSTRING(KK.CREATE_DATE, 5, 2) + '-' + SUBSTRING(KK.CREATE_DATE, 7, 2) 录单时间, 
KK.CREATOR 录单人编号, MV002 录单人名称, KK.TD001 订单单别, KK.TD002 订单单号, KK.TD003 订单序号, KK.TD020 描述备注 

FROM  
(
SELECT COPTD.CREATOR CREATOR, COPTD.CREATE_DATE CREATE_DATE, TD001 , TD002 , TD003 , TD020  FROM COPTD
WHERE 1=1
AND (TD020 LIKE '%POP%' OR TD020 LIKE '%京东%' OR TD020 LIKE '%菜鸟%')
AND TD020 NOT LIKE '%HAM%' AND TD020 NOT LIKE '%AW%' AND TD020 NOT LIKE '%山东%'
AND TD016 != 'y'
) KK
INNER JOIN CMSMV ON MV001 = KK.CREATOR
WHERE 1=1 
--AND (KK.TD020 NOT LIKE '%菜鸟条码%' AND KK.TD020 NOT LIKE '%京东POP条码%' AND KK.TD020 NOT LIKE '%京东条码%' AND KK.TD020 NOT LIKE '%京东无码%')
ORDER BY SUBSTRING(KK.CREATE_DATE, 1, 4) + '-' + SUBSTRING(KK.CREATE_DATE, 5, 2) + '-' + SUBSTRING(KK.CREATE_DATE, 7, 2), 
KK.TD001, KK.TD002, KK.TD003