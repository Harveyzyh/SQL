USE COMFORT

SELECT DISTINCT TD001 订单别, TD002 订单号, TD003 序号, TD004 产品品号, TD005 规格, TD053 配置方案
, TB001 工单别, TB002 工单号
, TB003 材料品号, MB002 材料品名, TB006 工艺
, TE001 领料单别, TE002 领料单号 
FROM COPTD
LEFT JOIN MOCTA ON TA026 = TD001 AND TA027 = TD002 AND TA028 = TD003
LEFT JOIN MOCTB ON TA001 = TB001 AND TA002 = TB002
LEFT JOIN INVMB ON TB003 = MB001
LEFT JOIN MOCTE ON TE011 = TB001 AND TE012 = TB002
WHERE 1 = 1
AND TD001 = '2212'  --2212-DY000822-0001
AND TD002 = 'DY000822'
AND TD003 = '0001'
--AND TE001 = '5401'
--AND TE002 = '18042879'
AND TB006 IN ('0101','0102','0104','0105')

ORDER BY TD001, TD002, TD003, TD053


SELECT * FROM MOCTC
WHERE TC001 = '5401'
AND TC002 = '18042879'

SELECT * FROM INVMB
WHERE MB001 = '10720501'


