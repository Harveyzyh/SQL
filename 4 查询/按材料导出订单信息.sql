SELECT DISTINCT MB2.MB001 材料品号, MB2.MB002 材料品名, MB2.MB003 材料规格, TC001 订单单别, TC002 订单单号, TC003 订单日期, TC004 客户编号, MA002 客户名称, 
MB1.MB001 产品品号, MB1.MB002 产品品名, MB1.MB003 产品规格, CAST(TD008 AS FLOAT) 订单数量
FROM COPTC
INNER JOIN COPTD ON TC001 = TD001 AND TC002 = TD002 
INNER JOIN COPMA ON MA001 = TC004 
INNER JOIN MOCTA ON TA026 = TD001 AND TA027 = TD002 AND TA028 = TD003 
INNER JOIN MOCTB ON TA001 = TB001 AND TA002 = TB002
INNER JOIN INVMB AS MB2 ON MB2.MB001 = TB003
INNER JOIN INVMB AS MB1 ON MB1.MB001 = TD004
WHERE 1=1
AND TA011 = 'Y'
AND TB003 IN ('3020502042', '3020501020', '3020502034')
ORDER BY MB2.MB001, TC004



