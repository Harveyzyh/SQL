SELECT DISTINCT TB001 AS W01, TB002 AS W02, TB003 AS W03, TB006 AS W04 FROM MOCTB
INNER JOIN MOCTA ON TB001 = TA001 AND TB002 = TA002 
INNER JOIN COPTH ON TA026 = TH014 AND TA027 = TH015 AND TA006 = TH004
WHERE NOT EXISTS ( SELECT 1 FROM MOCTE WHERE TB001 = TE011 AND TE012 = TB002 AND TE019 = 'Y' ) 
AND TH020 = 'N'
AND TB018 <> 'V'
AND (TB004 - TB005 ) > 0
ORDER BY W01, W02