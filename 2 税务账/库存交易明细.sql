
SELECT 
RTRIM(LA001) 品号, RTRIM(MB002) 品名, RTRIM(MB003) 规格, RTRIM(LA004) 单据日期, LA005 出入库类别, RTRIM(LA006) 单别, RTRIM(MQ002) 单据名称, RTRIM(LA007) 单号, RTRIM(LA008) 序号, RTRIM(LA009) 仓库, 
CONVERT(FLOAT, LA011) 交易数量, CONVERT(FLOAT, LA012) 单据单位成本, CONVERT(FLOAT, LA013) 金额, LA015 成本码, LA016 批号, LA017 '金额-材料', LA022 审核时间, LA024 对象, 
(CASE LA014 WHEN '1' THEN '1 入库' WHEN '2' THEN '2 销货' WHEN '3' THEN '3 领用' WHEN '4' THEN '4 调拨' WHEN '5' THEN '5 调整' ELSE LA014 END) 交易码
FROM INVLA 
INNER JOIN INVMB ON MB001 = LA001
LEFT JOIN CMSMQ ON MQ001 = LA006
WHERE 1=1
--AND LA001 = '3010305002'
AND LA001 = '3010301002'
AND LA004 >= '20190901'
AND LA009 = 'LY051'
AND LA015 = 'Y'
--AND LA005 = 1

ORDER BY LA001, LA004, LA006, LA007

