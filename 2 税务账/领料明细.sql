SELECT TC003 单据日期, TC014 审核日期,  TC001 单别, TC002 单号, TE003 序号, TC009 单头审核码, TE019 单身审核码, 
TE004 品号, MB002 品名, MB003 规格, TE005 领退料数量, TE010 批号, TE011 工单别, TE012 工单号 
FROM MOCTC 
INNER JOIN MOCTE ON TE001=TC001 AND TE002 = TC002
LEFT JOIN INVMB ON TE004 = MB001
WHERE 1=1
AND TC014 BETWEEN '20181101' AND '20181231'

ORDER BY TC014, TC001, TC002, TE003


