

-- 600102 主营业务收入-内销收入 内销销售
-- 600103 主营业务收入-电商收入 电商销售
SELECT * 
-- UPDATE ACRTB SET TB013='600102' 
FROM ACRTB INNER JOIN ACRTA ON TB001=TA001 AND TB002=TA002 
INNER JOIN INVMB ON TB039=MB001
WHERE SUBSTRING(TA038,1,6)='202002' AND TB001='6103' AND ((MB025='M' AND TB039 LIKE '1%') OR TB039 LIKE '6%' OR TB039 LIKE '7%')
AND TB013 != '600102'

