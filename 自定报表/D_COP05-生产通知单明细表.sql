SELECT 
''AS X000, 
(CASE WHEN TC004='0118' THEN '内销' ELSE '外销' END) X002,
(RTRIM(COPTD.TD001) +'-'+ RTRIM(COPTD.TD002) +'-'+COPTD.TD003+(CASE WHEN COPTF.UDF51='1' THEN '(新增)' WHEN COPTF.UDF51='0' THEN '(变更)' ELSE '' END)) AS X001,
COPMA.MA002 AS COPMAMA002,
COPTC.TC015 AS COPTCTC015,
(CASE WHEN COPTF.TF003 IS NULL THEN '' WHEN COPTF.TF003 IS NOT NULL AND COPTF.TF017 = 'Y' THEN '指定结束'+':'+'变更版本号'+COPTF.TF003+'_'+COPTF.UDF11 ELSE '变更版本号'+COPTF.TF003+'_'+COPTF.UDF11 END) AS X006,
COPTD.TD013 AS COPTDTD013,
COPTD.UDF03 AS COPTDUDF03,
COPTD.UDF01 AS COPTDUDF01,
COPTD.TD005 AS COPTDTD005,
COPTD.UDF08 AS COPTDUDF08,
COPTD.TD006 AS COPTDTD006,
COPTD.TD008 AS COPTDTD008,
COPTD.TD024 AS COPTDTD024,
COPTD.TD053 AS COPTDTD053,
COPTQ.TQ003 AS COPTQTQ003,
(COPTQ.UDF07+COPTD.TD020) AS X008,
COPTD.TD204 AS COPTDTD204,
COPTC.TC035 AS COPTCTC035,
CMSMV.MV002 AS CMSMVMV002,
COPTC.TC012 AS COPTCTC012,
COPTD.TD014 AS COPTDTD014,
--RTRIM( CMSME.ME002 ) AS X009,
(CASE WHEN TC004='0118' THEN INVMB.UDF04 ELSE INVMB.UDF05 END) AS X003,
COPTD.UDF05 AS COPTDUDF05,
COPTD.UDF10 AS COPTDUDF10,
(CASE WHEN COPTC.UDF09='否' THEN '' ELSE '是' END) AS X005,
SUBSTRING(COPTD.CREATE_DATE,1,12) AS X004,
RTRIM( INVMB.UDF12 ) AS X010,
RTRIM(COPTQ.UDF07) As X011,
(CASE WHEN K.TDUDF52 IS NOT NULL THEN '是' ELSE '' END) X007
FROM COPTD AS COPTD
Left JOIN COPTC AS COPTC On COPTD.TD001=COPTC.TC001 and COPTD.TD002=COPTC.TC002
Left JOIN COPTQ AS COPTQ On COPTD.TD053=COPTQ.TQ002 and COPTD.TD004=COPTQ.TQ001
Left JOIN COPMA AS COPMA On COPTC.TC004=COPMA.MA001
Left JOIN CMSMV AS CMSMV On COPTC.TC006=CMSMV.MV001
LEFT JOIN INVMB AS INVMB ON COPTD.TD004=INVMB.MB001
Left JOIN COPTF AS COPTF On COPTD.TD001=COPTF.TF001 and COPTD.TD002=COPTF.TF002 and COPTD.TD003=COPTF.TF104
LEFT JOIN (SELECT TD001 AS TDTD001, TD002 AS TDTD002, TD003 AS TDTD003, TD013 AS TDTD013, UDF52 AS TDUDF52 FROM COPTD AS COPTD) K ON TDTD001 = TD001 AND TDTD002 = TD002 AND TDTD003 = TD003 AND CONVERT(INT, SUBSTRING(COPTD.CREATE_DATE, 1, 8)) = CONVERT(INT, TDUDF52) AND CONVERT(INT, TD013) - CONVERT(INT, TDUDF52) <=2
LEFT JOIN CMSME AS CMSME ON CMSME.ME001 = INVMB.MB445
WHERE 1=1 AND (COPTC.TC027 = 'Y') AND COPTD.TD004 NOT LIKE '6%' AND COPTD.TD004 NOT LIKE '7%'
--AND RTRIM(TD002) IN ('88000734')
--AND RTRIM(TD002) > '061666' AND TD001 = '2205'
ORDER BY TD002,TD003